"""Was sind die Vorteile von objektorientierter Programmierung?
Wann sollte man sie nutzen?"""

""" Vorteile der objektorientierten Programmierung:
1. Durch die Möglichkeit der Vererbung kann schlanker und übersichtlicher Code entwickelt werden.

2. Vererbung ermöglicht es, Code wiederzuverwenden. 
Es muss nicht jedes Mal das Rad neu erfunden werden.

3. Polymorphismus erlaubt es, allgemeine Programmfunktionen wie das Atmen von Tieren zu definieren,
ohne dass man sich zu Beginn um spezielle Ausprägungen wie bei Enten, Robben und Alpakas kümmern muss. 

4. Da die Welt um uns herum aus Objekten besteht, hat man intuitiv ein Gefühl dafür, 
wie man diese in Code abbilden kann. Das vereinfacht die Entwicklung komplexer Anwendungen.

5. Die Abstraktion erleichtert die Wartung des entwickelten Codes.

6. Durch die Datenkapselung ist man in der Lage genau zu steuern, 
wer in einer Anwendung was genau machen darf.

Man sollte objektorientierte Programmierung verwenden, wenn größere Softwareprojekte umsetzt werden sollen.
Für kleinere Programme ist dieses Programmierparadigma nur manchmal geeignet. Hier besteht die Gefahr des 
Over-Engineerings.
"""

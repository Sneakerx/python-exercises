"""List Operations Exercise."""

print([0, 1, 2] * 2)  # Output: [0, 1, 2, 0, 1, 2]
print(3 * [True, False])  # Output: [True, False, True, False, True, False]
print([1, 1] + [2, 2] + [3, 3])  # Output: [1, 1, 2, 2, 3, 3]
print(["Hallo"] + ["Python", "!"])  # Output: ['Hallo', 'Python', '!']
print(2 * ([True] + [True, False]))  # Output: [True, True, False, True, True, False]

"""List Operations Exercise."""

print(",".join(["3", "14"]))  # Output: 3,14
print(".".join(["Max", "Mustermann"]))  # Output: Max.Mustermann
print("*".join(["1", "2", "3", "4", "5"]))  # Output: 1*2*3*4*5
print("".join(["F", "l", "o"]))  # Output: Flo
print("--".join(["-", "-", "-"]))  # Output: -------
